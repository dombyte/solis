// Get endpoint for a specific key
export function getEndpoint(key: string): RegisterConfig | null {
  return registerConfigs.find((c) => c.key === key) || null;
}

// Check if a key is configured
export function isConfigured(key: string): boolean {
  return registerConfigs.some((c) => c.key === key);
}

// Get all configured keys
export function getAllConfiguredKeys(): string[] {
  return registerConfigs.map((c) => c.key);
}

// Get register config by key
export function getRegisterConfig(key: string): RegisterConfig | null {
  return registerConfigs.find((c) => c.key === key) || null;
}
