// Register configuration
// Static configuration for all displayed registers with custom labels and endpoints

// Endpoint types for each register
export type RegisterEndpoint =
  | "data" // /api/v1/data/{key} - real-time values
  | "status" // /api/v1/status/{key} - status/fault register values
  | "daily" // /api/v1/daily/{key} - daily values
  | "monthly" // /api/v1/monthly/{key} - monthly values
  | "yearly" // /api/v1/yearly/{key} - yearly values
  | "total"; // /api/v1/total/{key} - lifetime/total values

// Configuration for a single register
export interface RegisterConfig {
  key: string;
  name: string; // Custom display name (not from API)
  endpoint: RegisterEndpoint;
  unit?: string; // Optional: override unit from API
  description?: string; // Optional: description for tooltip
}

// Configuration for a card/group of registers
export interface CardConfig {
  title: string;
  registers: RegisterConfig[];
}

